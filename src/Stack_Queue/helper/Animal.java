public class Animal {

    int order;
    String name;

    public Animal(String name) {
        this.order = order;
        this.name = name;
    }

    public int getOrder() {
        return order;
    }

    public String getName() {
        return name;
    }

    public void setOrder(int order) {
        this.order = order;
    }
}
